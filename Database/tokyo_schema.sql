--DROP TABLE stock_price; 

CREATE TABLE stock_price(
	RowId	VARCHAR,
	Date	DATE,
	SecuritiesCode	INT,
	Open	FLOAT,
	High	FLOAT,
	Low	 FLOAT,
	Close	 FLOAT,
	Volume	 INT,
	AdjustmentFactor FLOAT,
	SupervisionFlag	 BOOLEAN,
	ExpectedDividend FLOAT,
	Target	 FLOAT,
	PRIMARY KEY (RowId),
	FOREIGN KEY (SecuritiesCode) REFERENCES stock_list (SecuritiesCode)
	);

